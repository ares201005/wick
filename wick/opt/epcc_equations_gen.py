    T2 += -1.0*einsum('Ibi,Iaj->abij', g.vo, U11old)
    T2 += -1.0*einsum('Iaj,Ibi->abij', g.vo, U11old)
    T2 += 1.0*einsum('Ibj,Iai->abij', g.vo, U11old)
